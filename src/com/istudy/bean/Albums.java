	package com.istudy.bean;

public class Albums {
	private int id;
	private int played;
	private int highscore;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int isPlayed() {
		return played;
	}
	public void setPlayed(int played) {
		this.played = played;
	}
	public int getHighscore() {
		return highscore;
	}
	public void setHighscore(int highscore) {
		this.highscore = highscore;
	}
	
}
